package com.kmss.practice;

// Print 10 to 1 numbers
public class ReverseNumbers {

	public static void main(String[] args) 
	{
      for(int i=10;i>=1;i--)
      {
    	  if(i>1)
    	  {
    	  System.out.print(i+",");
    	  }
    	  else
    		  System.out.println(i+".");
      }
	}

}
